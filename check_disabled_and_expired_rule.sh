  DETAIL=$(mgmt_cli show-time uid "$TUID" details-level full \
             --format json --session-file session.json)
  ISO=$(echo "$DETAIL" | jq -r '.end["iso-8601"] // empty')
  if [[ -n "$ISO" ]]; then
    TS=$(date --date="$ISO" +%s)
  else
    POS=$(echo "$DETAIL" | jq -r '.end.posix // empty')
    (( POS )) && TS=$(( POS/1000 )) || continue
  fi
  TIME_ENDS["$TUID"]=$TS
done

